package com.anurag;

public class Human {
    private int age;
    private int weight;

    public void setAge(int age) {
        this.age = age;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getAge() {
        return age;
    }

    public int getWeight(){
        return weight;
    }
}
